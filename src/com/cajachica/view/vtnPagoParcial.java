    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ReembolsosDao rDado = new ReembolsosDao();
        try {
            ResultSet rs = rDado.sumarMontoTotal(txtFinanciador.getText());
            if (rs != null) {
                while (rs.next()) {
                    txtMontoDevolver.setText(rs.getString("monto"));
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se encuantra monto a restituir", null, JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al realizar la suma del monto total a devolver." + ex.getMessage(), null, JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

