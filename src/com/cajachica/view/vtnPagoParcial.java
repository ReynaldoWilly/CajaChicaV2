    private void txtMontoParcialKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMontoParcialKeyTyped
        // TODO add your handling code here:
        double var1 = Double.valueOf(txtMontoDevolver.getText());
        double var2 = Double.valueOf(txtMontoParcial.getText());
        double var3 = var1 - var2;
        if (txtMontoParcial.getText().length() == 0) {
            var2 = 0;
        }
        txtSaldoMonto.setText(String.valueOf(var3));
    }//GEN-LAST:event_txtMontoParcialKeyTyped


